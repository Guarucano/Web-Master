            <h2 class="blog-post-title">1er Concurso de Discurso GOEN</h2>
            <p class="blog-post-meta">Martes 10 de diciembre de 2013 por <a href="../../acerca.php">Grupo GOEN Maracaibo</a></p>
          
			<p class="text-justify">¿Qué tal va tu estudio en el japonés? ¿Ya has participado en el club de conversación? ¿Tienes N5? ¿Hablas mejor que un N5? ¡Pruébalo! Inscríbete y participa en nuestro primer concurso de discurso,  a celebrarse el 12 de Enero del 2014. ¿Cómo? ¡Muy sencillo!</p>
			<blockquote>
				<ol>
					<li>Se aceptará a cualquier estudiante del GOEN que sea mayor de edad. </li>
					<li>No tendrá costo alguno.</li>
					<li>Inscríbase en este link: DISCURSO.</li>
					<li>Recibirá un correo con las instrucciones y el tema.</li>
					<li>Deberá enviarnos un correo al GOEN con su discurso en Japonés y en Español, además de su nombre y apellido.  <b class="text-danger">FECHA LÍMITE</b>: 7 de Enero 2014. Recibirá entonces un segundo correo con el horario de presentaciones. </li>
					<li>El discurso no podrá tener más de una página, y deberá ser escrito en letra Arial 12. </li>
					<li>La presencia, la actitud y la puntualidad serán puntos a evaluar el día de la presentación. </li>
					<li>Lo no dispuesto en estas bases será resuelto por el GOEN.</li>
				</ol>
			</blockquote>

			<p class="text-justify">Luego, comparte esta noticia con tus amigos para tener un gran día de discursos. <br><br>

 Los ganadores de este concurso serán los elegidos para representar al GOEN en el Concurso de Discurso de la Semana Cultural del Japón en Caracas, edición 2015, pudiendo aspirar a un viaje cultural a Japón de resultar ganador. </p>
            
