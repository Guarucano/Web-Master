      <p class="text-justify">
        Ha llegado esa esperada época del año... ¡La hora de inscribirse en la aventura de enfrentar al Noken y probar tus habilidades en la lengua japonesa! Este año, igual que el anterior, el GOEN se encargará de llevar a gran parte de los estudiantes de japonés de Maracaibo, con un procedimiento muy parecido al vivido en el 2012.

        Como sabrán, este examen es realizado en Caracas, el primer domingo de diciembre, al mismo tiempo que es realizado en varias partes del mundo. Desde hace dos años, el GOEN ha sido constante al llevar estudiantes a esta prueba y vencerla con muy buenos resultados. Esta vez estamos orgullosos de decir: ¡Presentaremos todos los niveles! Así es, habrá aspirantes a N5, N4, N3, N2 y N1. ¿Quieres ser tú uno de esos aspirantes? Solo debes presentar el Pre-Noken, un simulacro del Noken que nos permitirá evaluar tu posible rendimiento en el examen real; y cumplir los siguientes requisitos:
        
        1) Estar o haber estado en menos de 6 meses en algún curso de lengua de GOEN
        2) Estar inscrito a través la planilla digital y seleccionar el nivel de examen a tomar
        3) Cancelar el costo del material del examen el día del examen (30bsf)
        
        Tal como lo dijimos: es un simulacro. No necesariamente debes aprobarlo para poder inscribirte en el Noken, solo debes llegar a un nivel suficiente para que, con nuestros cursos, puedas completar el conocimiento faltante.
        
        La fecha del simulacro será: 3 y 4 de agosto. Los horarios serán enviados al correo que coloquen en la planilla de inscripción según su turno de examen.
        
        ¡Ánimo! ¡Esta es la prueba más importante de la lengua japonesa para extranjeros en el mundo! ¡No pierdas la oportunidad!
        
